#include "libft.h"
#include <stdlib.h>

void	ft_bzero(void *s, size_t n)
{
	unsigned char *i;

	i = (unsigned char*)s;
	if (n > 0)
	{
		while (n > 0)
		{
			*i++ = '\0';
			n--;
		}
	}
	else if (n == 0)
	{
	}
}
