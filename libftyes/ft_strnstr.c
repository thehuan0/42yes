#include "libft.h"
#include <stdlib.h>

char	*ft_strnstr(const char *haystack, const char *needle, size_t len)
{
	int		i;
	char	*hay;

	i = 0;
	hay = (char*)haystack;
	if (*needle == '\0')
		return (hay);
	while (*haystack && len)
	{
		if (*haystack == needle[i])
			i++;
		else
			i = 0;
		if (needle[i] == '\0')
			return ((char*)(haystack - i + 1));
		haystack++;
		len--;
	}
	return (NULL);
}
